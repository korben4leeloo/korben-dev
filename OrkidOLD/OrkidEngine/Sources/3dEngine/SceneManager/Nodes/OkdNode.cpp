#include	"OkdNode.h"

//-----------------------------------------------------------------------------
// Name:		OkdNode constructor
//
// Created:		2013-08-26
//-----------------------------------------------------------------------------
OkdNode::OkdNode()
: _pParentNode( 0 )
{
	
}

//-----------------------------------------------------------------------------
// Name:		OkdNode destructor
//
// Created:		2013-08-26
//-----------------------------------------------------------------------------
OkdNode::~OkdNode()
{

}

//-----------------------------------------------------------------------------
// Name:		setParentNode
//
// Created:		2013-08-26
//-----------------------------------------------------------------------------
inline void	OkdNode::setParentNode( OkdNode* pParentNode )
{
	_pParentNode = pParentNode;
}

//-----------------------------------------------------------------------------
// Name:		getParentNode
//
// Created:		2013-08-26
//-----------------------------------------------------------------------------
inline const OkdNode*	OkdNode::getParentNode() const
{
	return	( _pParentNode );
}

//-----------------------------------------------------------------------------
// Name:		addChildNode
//
// Created:		2013-08-26
//-----------------------------------------------------------------------------
inline void	OkdNode::addChildNode(OkdNode *	pChildNode)
{
	_childrenNodes.push_back( pChildNode );
	pChildNode->setParentNode( this );
}
