#include	"Quaternionf.h"

#include	KOSMO_CORE_H(Math/Vector3f)

//-----------------------------------------------------------------------------
// Name:		Quaternionf constructor
//
// Created:		2013-08-26
//-----------------------------------------------------------------------------
Quaternionf::Quaternionf()
{
	
}

//-----------------------------------------------------------------------------
// Name:		Quaternionf constructor
//
// Created:		2013-08-26
//-----------------------------------------------------------------------------
Quaternionf::Quaternionf(const Vector3f&	vAxis, 
						 const float		fAngle)
{
	_q = Eigen::Quaternionf( Eigen::AngleAxisf( fAngle, vAxis._v ) );
}

